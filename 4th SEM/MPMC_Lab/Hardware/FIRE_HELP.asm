.MODEL SMALL

.CODE
PB EQU 0D801H
PC EQU 0D802H
CR EQU 0D803H
FIRE DB 86H, 88H, 0FEH, 8EH 
HELP DB 8CH, 0C7H, 86H, 89H

.CODE
MOV AX, @DATA
MOV DS, AX
MOV DX, CR
MOV AL ,80H
OUT DX, AL

MOV CX, 10
RPT:PUSH CX
LEA SI ,FIRE
CALL DISPLAY
CALL DELAY
LEA SI , HELP
CALL DISPLAY
CALL DELAY
POP CX
LOOP RPT

MOV AH , 4CH
INT 21H

DELAY PROC 
PUSH CX
PUSH DX
MOV CX, 1000H
OUTLOOP:MOV DX , 1000H
INLOOP:DEC DX
JNZ INLOOP
LOOP OUTLOOP
POP DX
POP CX
RET
DISPLAY ENDP

DISPLAY PROC
MOV BL ,4
BACK2: MOV CL ,8
MOV AL ,[SI]
BACK1:ROL AL,1
MOV DX, PB
OUT DX, AL
PUSH AX
MOV AL ,1
MOV DX ,PC
OUT DX, AL
DEC AL
OUT DX, AL
POP AX
DEC CL
JNZ BACK1
INC SI
DEC BL
JNZ BACK2
RET
DISPLAY ENDP
END
