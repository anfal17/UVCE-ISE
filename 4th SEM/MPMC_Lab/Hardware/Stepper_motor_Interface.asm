.MODEL SMALL
.DATA
PA EQU 0D800H
CR EQU 0D803H



.CODE
MOV AX , @DATA
MOV DS, AX
MOV AL , 80H
MOV DX , CR
OUT DX , AL

MOV CX, 10
MOV DX , PA
MOV AL ,11H

L1: OUT DX , AL
CALL DELAY
ROR AL ,1
LOOP L1

MOV AH, 4CH
INT 21H

DELAY PROC
PUSH CX
PUSH DX
MOV CX, 1000H
OUTLOOP:MOV DX, 1000H
INLOOP:DEC DX
JNZ INLOOP
LOOP OUTLOOP
POP DX
POP CX
RET
DELAY ENDP
END

